    class Solution {
        public int[][] updateMatrix(int[][] mat) {
            int[][] ret = new int[mat.length][mat[0].length];
            boolean start = false;

            int startX = -1;
            int startY = -1;
            for (int i = 0; i < ret.length; i++) {
                for (int j = 0; j < ret[0].length; j++) {
                    ret[i][j] = -1;
                    if (start && mat[i][j] == 1) {
                        startX = i;
                        startY = j;
                    }
                    if (mat[i][j] == 0) {
                        ret[i][j] = 0;
                        start = true;
                    }
                }
            }

            if (startX < 0 || startY < 0) {
                return mat;
            }

            for (int i = startX; i < startX + mat.length; i++) {
                for (int j = startY; j < startY + mat[0].length; j++) {
                    int x = i >= mat.length ? i - mat.length : i;
                    int y = j >= mat[0].length ? j - mat[0].length : j;

                    boolean[][] used = new boolean[mat.length][mat[0].length];
                    ret[x][y] = bfs(mat, x, y, used, 0, ret);
                }
            }
            return ret;
        }

        int direction(int x, int y, int stepCnt, List<Integer> nextNodes, int[][] mat, boolean[][] used) {
            if (x >= 0 && x < mat.length && y >= 0 && y < mat[0].length && !used[x][y]) {
                nextNodes.add(x);
                nextNodes.add(y);
                if (mat[x][y] == 0) {
                    used[x][y] = false;
                    return stepCnt + 1;
                }
            }
            return -1;
        }

        int bfs(int[][] mat, int x, int y, boolean[][] used, int stepCnt, int[][] caled) {
            if (mat[x][y] == 0) {
                return stepCnt;
            }
            if (caled[x][y] >= 0) {
                return stepCnt + caled[x][y];
            }
            used[x][y] = true;

            List<Integer> nextNodes = new ArrayList<Integer>();

            int up = direction(x - 1, y, stepCnt, nextNodes, mat, used);
            int down = direction(x + 1, y, stepCnt, nextNodes, mat, used);
            int left = direction(x, y - 1, stepCnt, nextNodes, mat, used);
            int right = direction(x, y + 1, stepCnt, nextNodes, mat, used);

            used[x][y] = false;
            if (up > 0) return up;
            if (down > 0) return down;
            if (right > 0) return right;
            if (left > 0) return left;

            used[x][y] = true;
            int min = 999999;
            for (int i = 0; i < nextNodes.size(); i += 2) {
                int step = bfs(mat, nextNodes.get(i), nextNodes.get(i + 1), used, stepCnt, caled);
                min = Math.min(step + 1, min);
                if (step == 0) break;
            }

            used[x][y] = false;
            return Math.min(min, 999999);
        }
    }
